package com.etop.website.bean;

public class EtopSubDomain {
	
	private String hostName;
	
	private String parkGroupId;
	
	private String parkGroupName;

	public String getHostName() {
		return hostName;
	}

	public void setHostName(String hostName) {
		this.hostName = hostName;
	}

	public String getParkGroupId() {
		return parkGroupId;
	}

	public void setParkGroupId(String parkGroupId) {
		this.parkGroupId = parkGroupId;
	}

	public String getParkGroupName() {
		return parkGroupName;
	}
	
	public void setParkGroupName(String parkGroupName) {
		this.parkGroupName = parkGroupName;
	}
}
